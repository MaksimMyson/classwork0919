ціна_приставки = float(input("Введіть вартість однієї ігрової приставки: "))
кількість_приставок = int(input("Введіть кількість приставок: "))
знижка_відсоток = float(input("Введіть відсоток знижки: "))
загальна_вартість = ціна_приставки * кількість_приставок * (1 - знижка_відсоток / 100)
print(f"Загальна сума замовлення: {загальна_вартість} грн")
